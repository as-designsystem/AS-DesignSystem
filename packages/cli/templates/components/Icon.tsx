export type IconName = typeof availableIcons[number];

export interface IconProps {
  name: string;
  size?: number | string;
  className?: string;
  color?: string;
}

/**
 * Icon Component
 *
 * Displays an icon by name. The icon can be resized using the size prop.
 *
 * @example
 * ```tsx
 * <Icon name="AIR_fleet" size={24} />
 * <Icon name="add" size="32px" color="var(--primary-default)" />
 * ```
 */
export function Icon({ name, size = 24, className = '', color }: IconProps) {
  const iconSize = typeof size === 'number' ? `${size}px` : size;

  if (name === 'blank') {
    return (
      <span
        className={`icon icon-blank ${className}`}
        style={{ display: 'inline-flex', width: iconSize, height: iconSize, flexShrink: 0 }}
        aria-hidden="true"
      />
    );
  }

  const svgContent = iconMap[name];
  const defaultColor = color || 'var(--primary-default, var(--sea-blue-70, #063b9e))';

  if (!svgContent) {
    console.error(`Icon "${name}" not found. Available icons: ${Object.keys(iconMap).join(', ')}`);
    return null;
  }

  // Replace fill and stroke attributes with currentColor, but preserve fill="none"
  // This is important for SVGs that use fill="none" on the root element
  let processedSvg = svgContent
    .replace(/fill="(?!none")[^"]*"/g, 'fill="currentColor"')
    .replace(/stroke="(?!none")[^"]*"/g, 'stroke="currentColor"')
    .replace(/<svg/, `<svg width="${iconSize}" height="${iconSize}"`);

  // If the root <svg> has no fill attribute, add fill="currentColor" so paths without
  // explicit fill inherit it (SVG default fill is black, not currentColor)
  if (!/^<svg[^>]*fill="/.test(processedSvg)) {
    processedSvg = processedSvg.replace(/<svg/, '<svg fill="currentColor"');
  }

  return (
    <span
      className={`icon icon-${name} ${className}`}
      style={{
        display: 'inline-flex',
        color: defaultColor,
        lineHeight: 0,
      }}
      dangerouslySetInnerHTML={{ __html: processedSvg }}
    />
  );
}

export default Icon;
